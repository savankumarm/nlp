package nlp.com;

public class CorefObj {
	int corefId;
	String coref;
	String corefXML;
	String refId;
	
	
	public String getCorefXML() {
		return corefXML;
	}
	public void setCorefXML(String corefXML) {
		this.corefXML = corefXML;
	}
	public int getCorefId() {
		return corefId;
	}
	public void setCorefId(int corefId) {
		this.corefId = corefId;
	}
	public String getCoref() {
		return coref;
	}
	public void setCoref(String coref) {
		this.coref = coref;
	}
	public String getRefId() {
		return refId;
	}
	public void setRefId(String refId) {
		this.refId = refId;
	}
	
	
}
